    public partial class viewcompany : Form
    {
        SqlConnection con = new SqlConnection(@"Data Source=Kamalesh;Initial Catalog=placement;Integrated Security=True");
        public viewcompany()
        {
            InitializeComponent();
        }


        private void viewcompany_Load_1(object sender, EventArgs e)
        {
            con.Open();
            SqlCommand cmd = con.CreateCommand();
            cmd.CommandType = CommandType.Text;
            cmd.CommandText = "select * from company";
            cmd.ExecuteNonQuery();
            DataTable dt = new DataTable();
            SqlDataAdapter da = new SqlDataAdapter(cmd);
            da.Fill(dt);
            dataGridView1.DataSource = dt;
            con.Close();
        }
    }
